function redirect(id){
        if (id=='llogin'){
                window.location.href="llogin.html"
        }
        else if(id=='lsignup'){
                window.location.href="lsignup.html"
        }
        else if(id=='hlogin'){
                window.location.href="hlogin.html"
        }
        else if(id=='hsignup'){
                window.location.href="hsignup.html"
        }
}